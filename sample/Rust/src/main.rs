use simple::中文模块;

/**
 * Rust 中文命名样例
 */

pub fn 中文函数(参数1: &str, 参数2: &str) {
    // 枚举示例
    let 喜欢的颜色 = 中文模块::颜色枚举::红色;
    println!("喜欢的颜色: {:?}", 喜欢的颜色);

    // 学生元组示例
    let 学生 = 中文模块::学生元组 {
        姓名: "李四".to_string(),
        年龄: 17,
        班级: "高二一班".to_string(),
    };
    println!("学生信息: {}, {}岁, {}", 学生.姓名, 学生.年龄, 学生.班级);

    println!("{}", 参数1);
    println!("{}", 参数2);
}

