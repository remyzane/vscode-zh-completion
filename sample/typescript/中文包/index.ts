import { 颜色枚举, 学生元组 } from './中文模块';

function 中文函数(参数: { [key: string]: any } = {}): void {
    // 枚举示例
    console.log(`喜欢的颜色: ${颜色枚举[颜色枚举.红色]}`);

    const 学生 = new 学生元组('李四', 17, '高二一班');
    console.log(`学生信息: ${学生.姓名}, ${学生.年龄}岁, ${学生.班级}`);

    // 处理传入参数
    if (参数.参数1) {
        console.log(`参数1: ${参数.参数1}`);
    }
    if (参数.参数2) {
        console.log(`参数2: ${参数.参数2}`);
    }
}

export {
    颜色枚举,
    学生元组,
    中文函数
};